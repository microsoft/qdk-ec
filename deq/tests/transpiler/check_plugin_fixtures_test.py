def _transpile_and_strip(text: str) -> bytes:
    """Parse, transpile, strip tags, return serialized protobuf bytes."""
    qfile = parse(text)
    library = build_jit_library(qfile)
    stripped, _ = strip_jit_library(library)
    return stripped.SerializeToString()


@pytest.mark.parametrize(
    "fixture_dir, template, mako_vars, ref_stem",
    _CASES,
    ids=[c[3] for c in _CASES],
)
def test_check_plugin_matches_reference(
    fixture_dir: str,
    template: str,
    mako_vars: dict[str, str],
    ref_stem: str,
) -> None:
    dirpath = FIXTURES_DIR / fixture_dir
    template_path = dirpath / template
    ref_path = dirpath / f"{ref_stem}.ref.deq"

    template_text = template_path.read_text(encoding="utf-8")
    rendered = render_mako(template_text, mako_vars, template_dir=str(dirpath))
    original_bytes = _transpile_and_strip(rendered)

    ref_text = ref_path.read_text(encoding="utf-8")
    ref_bytes = _transpile_and_strip(ref_text)

    assert original_bytes == ref_bytes, (
        f"Transpiled output of {template} (with {mako_vars}) differs from "
        f"{ref_path.name}. If the change is intentional, regenerate the "
        f"reference file by running 'make' in {dirpath}."
    )
