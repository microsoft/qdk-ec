"""Regression tests for check plugins.

Each test case renders a Mako template with specific variables (selecting
a check plugin), transpiles the result, then compares the tag-stripped
protobuf output against a committed ``.ref.deq`` reference file that was
previously generated by ``deq annotate``.

To update reference files after an *intentional* plugin change, run the
``Makefile`` inside the corresponding fixture directory, review the diff,
and commit.
"""

